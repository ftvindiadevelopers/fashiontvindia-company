                            <h1><strong>Kashiff Khan Insights On Fashion Icon</strong></h1>



                            <p>In the age of Instagram <a href="#" target="_blank" rel="noreferrer noopener">fashion</a> influencers who influence and
                                inspire the style of the masses with their personability, following a fashion icon is like
                                an outdated concept. Surprisingly, many fashion-obsessed people still regard a <a href="#" target="_blank" rel="noreferrer noopener">fashion icon</a> as their ideal role model who has stood the
                                test of trending times. Not to mention the fact that the title of a fashion icon is not
                                something that can be thrown around lightly, especially if you belong to the fashion
                                industry. It is more than a name for someone you look up to for inspiration, ideas and for
                                the ingrained sartorial approach.</p>



                            <p>As a man who started his career as a 17-year-old boy and knew that one day he would grow up
                                to become a fashion legend, I resonate the most with the German wunderkind- Karl Lagerfeld.
                                &nbsp;He belonged to a generation that knew a fashion world that was still free of branding
                                and enormous luxury groups, a world still preserved from marketing and purely corporate
                                spirit. Karl Lagerfeld possessed something rare and very precious: freedom of expression and
                                an independent spirit.</p>



                            <p>The highest priest of fashion, Karl Lagerfeld had a demeanour and way of thinking that was
                                always forward-looking, uncompromising, restless-never satisfied and never afraid of writing
                                new pages. The frenetic Chanel designer showed how the leaders in fashion, whether on the
                                creative or business sides, must be relevant and that means taking risks and seeing things
                                before others and more importantly, doing it.</p>



                            <p>Lagerfeld had a penchant to do things he’d never done before, and as a result, he ended up
                                opening multiple new frontiers in an industry long bound by tradition. Just as he pushed the
                                fashion industry to work with other sectors, Lagerfeld wanted runway shows to be about more
                                than just clothing.</p>



                            <p>A brilliant stylist, an outstanding draughtsman and a skilled photographer, he has breathed
                                new life into all the brands he has worked with. Yet his greatest legacy — and perhaps the
                                most under-appreciated — was smashing the idea that creative freedom and commercial success
                                are mutually exclusive. In an industry where everyone had a particular point of view, he was
                                a bilingual, multi-brand generalist.</p>



                            <p>The designer’s sphere of influence extended far beyond the runways, and his multifaceted
                                abilities touched everything from branding to photography and filmmaking. Once quoted saying
                                that we must “embrace the present and invent the future,” his boundary-pushing
                                collaborations were also ahead of their time.</p>



                            <p>In keeping with the audacity of his designs, Lagerfeld succeeded in cultivating a highly
                                identifiable public persona. Even though he liked to reject his “workaholic” image, no one
                                in the history of fashion has come close to matching his range of innovation. Lagerfeld’s
                                powdered hair, dark glasses, Hilditch &amp; Key high-collared shirts, tailored black jacket
                                and thin black pants, fingerless gloves, and boots were all part of his irreverent brand
                                identity.</p>



                            <p>The self-transformation into an icon was an important aspect of branding and the presentation
                                of the brands he represented. At Chanel, Lagerfeld soon emerged as a fashion icon. He not
                                only resurrected a dying brand even after people advised him not to do it, but he also
                                helped revitalise the international fashion scene and is best known for launching Claudia
                                Schiffer and Kate Moss’ modelling careers.</p>



                            <p>As an individual who loves change and gets attached to nothing, Karl’s mantra in life was why
                                I started following him in the first place. Growing tired of his overweight appearance, he
                                dropped 90 pounds by going on a crash diet. It was a dramatic change that he made to change
                                his body image and enable him to wear some of the clothes he admired by other designers.</p>



                            <p>This drive to reinvent and reimagine yourself as often as necessary is a trait that must be
                                ticked off for someone who wants to sustain and stay relevant in the ever-changing fashion
                                industry.</p>



                            <p>Lagerfeld deserves to be recognised as a natural creative force whose genius came to dominate
                                a highly competitive industry that is by definition, constantly renewing and reinventing
                                itself.</p>



                            <p>In any case, great people never die, their names live in their heritage a long time after.
                                The massive image of Karl Lagerfeld that can be found hanging in my FashionTV India office
                                is a perfect example of this.</p>
